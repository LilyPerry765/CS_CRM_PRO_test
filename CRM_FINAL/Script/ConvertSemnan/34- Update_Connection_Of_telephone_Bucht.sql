SELECT top(10) TI.[FI_CODE]
      ,TI.[TEL_NUMBER]
	  ,CONVERT(NVARCHAR(MAX) , BB.[CEN_CODE]) + CONVERT(NVARCHAR(MAX) , TI.[TEL_NUMBER]) AS phoneNumber
	  ,BB.[BOOKHT_ID]
	  ,BB.[CEN_CODE]
      ,BB.[RADIF]
      ,BB.[TABAGHE]
      ,BB.[ETESALI]
      ,BB.[BOOKHT_TYPE]
      ,BB.[STATUS]
      ,BB.[BB_DATE]
      ,BB.[BB_HOUR]
	  ,BS.[ETS_ID]
      ,TI.[TEL_LAST_COUNTER_READ]
      ,TI.[TEL_COUNTER_START]
      ,TI.[TEL_STATUS]
      ,TI.[TEL_DATE]
      ,TI.[TEL_HOUR]
      ,TI.[CENTER_CODE]
      ,TI.[REQ_ID]
      ,VB.[STATUS]
	  ,BB.[BOOKHT_TYPE]
	  ,BB.[STATUS]
      ,BB.[CEN_CODE]
	  ,BS.[PCM_STATUS]
  FROM [ORACLECRM]..[TT].[TELEPHONEINFORMATION] AS TI 
       JOIN  [ORACLECRM]..[TT].[V_BOOKHT] AS VB ON TI.[FI_CODE] = VB.[FI_CODE]
	   JOIN  [ORACLECRM]..[TT].[BASE_BOOKHT] AS BB ON VB.[BOOKHT_ID] = BB.[BOOKHT_ID]
	   JOIN  [ORACLECRM]..[TT].[ETESALI] AS E ON TI.[FI_CODE] = E.[FI_CODE]
	   JOIN  [ORACLECRM]..[TT].[BASE_ETESALI] AS BS ON E.[ETS_ID] = BS.[ETS_ID]
	   JOIN  [ORACLECRM]..[TT].[CENTER] AS C ON C.[CEN_CODE] = BB.[CEN_CODE]
	--  WHERE BB.[BOOKHT_TYPE] IN (22) AND BS.[PCM_STATUS] = 1
--  ORDER BY ETS_ID
