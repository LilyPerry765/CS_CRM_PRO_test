using System;
using System.Collections.Generic;
using System.Configuration;
using System.Linq;
using System.Web;

namespace PendarPajouhAPI.Helpers
{
    public static class ConfigurationHelpers
    {
        public static string PendarPajouhApiUserName
        {
            get
            {
                return ConfigurationManager.AppSettings["PendarPajouhApiUserName"];
            }
        }

        public static string PendarPajouhApiPassword
        {
            get
            {
                return ConfigurationManager.AppSettings["PendarPajouhApiPassword"];
            }
        }
        public static string ShahkarWebApiIP
        {
            get
            {
                return ConfigurationManager.AppSettings["ShahkarWebApiIP"];
            }
        }
    }
}